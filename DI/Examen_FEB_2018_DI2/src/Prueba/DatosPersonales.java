    private JButton btnAceptar, btnCancelar;
    private TextFieldExtended txtNombre, txtApellidos;

    public DatosPersonales(JFrame parent, boolean par) {
        super(parent, "Datos Personales", true);
        this.setSize(400, 300);
        this.setLayout(new BoxLayout(getContentPane(), BoxLayout.Y_AXIS));

        txtNombre = new TextFieldExtended();
        txtApellidos = new TextFieldExtended();

        btnAceptar = new JButton("Aceptar");
        btnCancelar = new JButton("Cancelar");

        btnAceptar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (txtNombre.getText().isEmpty() || txtApellidos.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(DatosPersonales.this, "Complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(DatosPersonales.this, "Datos completados correctamente", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);
                    DatosPersonales.this.dispose();
                }
            }
        });

        btnCancelar.addActionListener(e -> DatosPersonales.this.dispose());

        this.add(new JLabel("Nombre:"));
        this.add(txtNombre);
        this.add(new JLabel("Apellidos:"));
        this.add(txtApellidos);
        this.add(btnAceptar);
        this.add(btnCancelar);
    }

