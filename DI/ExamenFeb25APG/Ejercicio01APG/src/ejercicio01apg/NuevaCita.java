    private void jTextFieldNombreClienteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldNombreClienteKeyTyped
        // TODO add your handling code here:
        // TODO add your handling code here:
        char c = evt.getKeyChar();

        // Verificamos si el carácter es una letra
        if (!Character.isAlphabetic(c)) {
            evt.consume(); // No permite caracteres que no sean letras
        }

        if (this.jTextFieldNombreCliente.getText().length() >= 10) {
            evt.consume(); // Evita que el usuario escriba más de 10 caracteres
        }
    }//GEN-LAST:event_jTextFieldNombreClienteKeyTyped

