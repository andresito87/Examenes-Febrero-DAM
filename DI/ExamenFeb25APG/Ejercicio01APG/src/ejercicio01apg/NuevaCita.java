    private void jButtonReservarCitaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonReservarCitaMouseClicked
        // TODO add your handling code here:
        String errores = "";

        if (this.jTextFieldNombreCliente.getText().isEmpty()) {
            errores += " \nError en el nombre del cliente";
        }

        if (this.jTextFieldApellidos.getText().isEmpty()) {
            errores += " \nError en los apellidos del cliente";
        }

        if (this.jTextFieldApellidos.getText().isEmpty()) {
            errores += " \nError en el teléfono";
        }
        
        if(!this.jRadioButtonPerro.isSelected()&&!this.jRadioButtonGato.isSelected()){
            errores += " \nNo has seleccionado un tipo de aninal";
        }
        
        if(!this.jCheckBoxConsulta.isSelected()&&!this.jCheckBoxRevision.isSelected()&&!this.jCheckBoxVacunacion.isSelected()){
            errores += " \nNo has seleccionado el motivo de la cita";
        }
        
        if(!errores.isEmpty()){
            JOptionPane.showMessageDialog(
                    null,
                    errores,
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE
            );
        }else{
            JOptionPane.showMessageDialog(rootPane, "Reserva realizada");
            this.limpiarFormulario();
            this.dispose();
        }
        
        

    }//GEN-LAST:event_jButtonReservarCitaMouseClicked

